// Loops in array 

var arr=[1,2,3,4,5,6,7,8,9]
console.log(arr)
for(var i = 0; i < arr.length;i++){
    console.log(arr[i])
}
// hear loop will take i as 0 in starting and goes till length of array in i++ meance increment order 



// forEach..........
arr.forEach(function(element){
    console.log(element)
})
// foreach will run every element individually



// while loop............
let A = 1;

while (A <= 10) {
  console.log(A);
  A++;
}
// while loop will run till this condition will be true if not true it will stop 