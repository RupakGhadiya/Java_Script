// Methods of array 


let myArr = ["Rupak", "Ghadiya", 23 , null, true]

// methods 
console.log(myArr.length)
// to pop first element from array 
myArr.pop()

// to push somthing into array to the end
myArr.push("hello")


// to shift last to second element to start and remove first element 
myArr.shift()

// to add existing element to front (will also display new length)
myArr.unshift("Ghadiya")

// convert array to string 
myArr.toString()

// to sort array 
myArr.sort()

